raio = float(input("Digite o valor do raio do círculo: "))

pi = 3.1415

raio_ao_quadrado = raio * raio

area = pi * raio_ao_quadrado

print(f"O valor do raio é: {raio:.2f}")
print(f"O valor de pi é: {pi}")
print(f"O raio ao quadrado é: {raio_ao_quadrado:.2f}")
print(f"A área do círculo é: {area:.2f}")